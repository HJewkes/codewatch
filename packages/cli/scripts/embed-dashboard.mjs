#!/usr/bin/env node
// Embed the built single-file dashboard (dashboard/dist/index.html) into a
// committed TS module as gzip+base64, so `graph dashboard` can ship it without
// a runtime file dependency. Regenerate after changing the dashboard:
//   pnpm --dir dashboard build && node packages/cli/scripts/embed-dashboard.mjs
import { readFileSync, writeFileSync } from "node:fs";
import { gzipSync } from "node:zlib";
import { resolve, dirname } from "node:path";
import { fileURLToPath } from "node:url";

const here = dirname(fileURLToPath(import.meta.url));
const src = resolve(here, "../../../dashboard/dist/index.html");
const out = resolve(here, "../src/commands/dashboard-template.ts");
const html = readFileSync(src);
const b64 = gzipSync(html, { level: 9 }).toString("base64");
const ts = `// AUTO-GENERATED by scripts/embed-dashboard.mjs — do not edit by hand.
// The codewatch dashboard single-file build, gzip+base64. Decoded at runtime.
import { gunzipSync } from "node:zlib";

const TEMPLATE_GZ_B64 =
  "${b64}";

/** The dashboard HTML template (reads window.__CODEWATCH__ if injected). */
export function dashboardTemplate(): string {
  return gunzipSync(Buffer.from(TEMPLATE_GZ_B64, "base64")).toString("utf8");
}
`;
writeFileSync(out, ts);
console.log(`embedded ${html.length} bytes html -> ${b64.length} b64 chars -> ${out}`);
